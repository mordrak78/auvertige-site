// Fonction utilitaire pour obtenir une image par catégorie
export const getImageByCategory = (category: keyof typeof images.creations, imageName: string) => {
  return images.creations[category][imageName as keyof typeof images.creations[typeof category]];
};

// Fonction utilitaire pour obtenir toutes les images d'une catégorie
export const getAllImagesByCategory = (category: keyof typeof images.creations) => {
  return Object.values(images.creations[category]);
};

// Fonction utilitaire pour obtenir un logo
export const getLogo = (logoName: keyof typeof images.logos) => {
  return images.logos[logoName];
};
